    private void RBtnFiltroStateChanged(javax.swing.event.ChangeEvent evt) {//GEN-FIRST:event_RBtnFiltroStateChanged
        // TODO add your handling code here:
         if (RBtnFiltro.isSelected()){
            campoCiudad.setEnabled(true);
            DateIni.setEnabled(true);
            DateFin.setEnabled(true);

            labelCiudad.setForeground(Color.BLACK);
            labelDateIni.setForeground(Color.BLACK);
            labelDateFin.setForeground(Color.BLACK);
            
            RbtnReq1.setSelected(false);
            RbtnReq2.setSelected(true);
            RbtnReq3.setSelected(false);
            RbtnReq1.setEnabled(false);
            //RbtnReq2.setEnabled(false);
            RbtnReq3.setEnabled(false);

        }else{
            campoCiudad.setEnabled(false);
            DateIni.setEnabled(false);
            DateFin.setEnabled(false);
            
            labelCiudad.setForeground(Color.GRAY);
            labelDateIni.setForeground(Color.GRAY);
            labelDateFin.setForeground(Color.GRAY);
            
            RbtnReq1.setEnabled(true);
            RbtnReq2.setEnabled(true);
            RbtnReq3.setEnabled(true);
            
         }
    }//GEN-LAST:event_RBtnFiltroStateChanged

